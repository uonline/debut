prison_guards_room = room {
	nam = 'Комната стражи';
	dsc = [[
		Ты в комнате стражи. Обстановка здесь довольно скудная:
		стол, пара табуретов, и спящий стражник в углу.
	]];
	obj = {
		'prison_doc';
		'prison_chest';
	};
	way = {
		'prison_hall';
	};
	entered = function()
		if _guard_just_died then
			_guard_just_died = false
			event 'guard body is hidden'
			return [[
				To fix (нужно прятать тело стражника в свою камеру, до этого взяв оттуда крысу
				, и ключом закрывать камеру)

				Ты втаскиваешь тело за дверь и оборачиваешься лишь за тем, чтобы
				наделать в штаны, потому что за дверью сидит ещё один стражник.
				Впрочем, при ближайшем рассмотрении тот оказывается спящим.
			]]
		end
	end;
}

