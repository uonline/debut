prison_chest = obj {
	nam = 'Сундук';
	dsc = [[
		Пренебрегая табуретами на внушительного вида сундуке покоится,
		привалившийся к стене, {стражник}. Тихонько похрапывая, он несёт тяжёлое
		бремя тюремной службы.
	]];
	act = [[
		Ты осторожно подходишь к стражнику поближе, и в нос бъёт сильный запах перегара.
		Стражник смертельно пьян.
	]];
	used = function(self, what)
		if what == rat then
			event 'got his stuff back'
			return [[
				Ты решительно сталкиваешь стражника с сундука.
				^
				Ты объясняешь крысе, что содержимое этого сундука очень дорого
				для тебя как память. Крыса проникается и вскрывает замок
				заклинанием. Ты находишь там свои вещи, кокаин,
				метамфетамин, кинжал, охотничий рог, ксарказ орка
				(что бы это ни значило) и немного субстрата магии.
				^
				Пора сваливать. Кажется, в коридоре была лестница.
			]]
		end
	end;
}
