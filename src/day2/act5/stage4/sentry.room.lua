-- Разрушенная кладка
tower_stage4_sentry_stone_heap = obj {
	nam = '';
	dsc = [[
		^
		Теперь ты замечаешь, что огромные размеры окна возникли из-за
		разрушения части стены. Под ногами у тебя валяются {останки каменной кладки}.
	]];
	act = function()
		if not have 'tower_stone' then
			take 'tower_stone';
			return [[
				Охваченный непонятной тревогой.
				-- Подбираешь камень.
			]];
		end;
		
		return [[
		]];
	end;
}

