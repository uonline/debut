-- Главарь подполья
thieves_leader = obj {
	nam = 'Главарь подполья';
	dsc = [[
		На кровати, лицом вниз лежит {главарь подполья}.
	]];
	act = function()
		-- Обыскиваем главаря подполья
		if not have tower_adviser_room_key then
			take 'tower_strange_notes';
			take 'tower_adviser_room_key';
			take 'tower_vicar_diary';
			thieves_leader_body:enable();
			thieves_leader:disable();
			return [[
				Ты подходишь к кровати, чтобы окончательно убедиться, что главарь мёртв.
				Постель вокруг тела перепачкана кровью, но следов борьбы нет.
				Ты переворачиваешь тело и видишь жуткую рану на шее.
				Рука главаря сжимает небольшую книжицу в кожанной обложке.
				В карманах ты обнаруживаешь ключ и смятую записку.
			]];
		end;

		return [[
			Ты решаешь больше не тревожить труп.
		]];
	end;
};

-- Тело главаря подполья
thieves_leader_body = obj {
	nam = 'Тело главаря воров';
	dsc = [[
		На кровати распростёрто тело главаря подполья.
	]];
}
thieves_leader_body:disable();

