	nam = 'Смятая записка';
	inv = [[
		Ты разворачиваешь скомканный клочёк бумаги и с трудом разбираешь мелкий
		почерк:
		^
		^
		Я решил не говорить тебе этого вслух, ты ведь сам прекрасно знаешь: у стен
		есть уши, даже если эти стены под землёй. И эти уши уже услышали лишнее. Они
		в курсе наших с тобой договорённостей, в курсе на кого ты работаешь.
		Хотя мне иногда кажется, ты даже не особо это скрываешь. Но сейчас не об этом.
		Я прибег к своим, как ты это называешь, фокусам, и выяснил кое-что. Тебе это
		не понравится, но в твоём подполье завёлся крот. Я не понял до конца, на кого
