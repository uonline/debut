lane = room {
	nam = 'Переулок';
	dsc = [[
		Ты стоишь в узком переулке. Между домов, на уровне вторых-третьих этажей,
		развешано бельё на верёвках, из-за чего здесь не особо светло.
		Воняет нечистотами и испражнениями.
		Ты вспоминаешь, что твоё детство прошло примерно в такой же обстановке:
		на узких улочках одного из городков Приграничья. В те годы судьба уберегла
		тебя от связей со всяким сбродом. Напротив, ты вырос и попытался стать солдатом.
		Сейчас, видимо, всё возвращается на круги своя.
	]];
	obj = {
		'lane_rouge_one';
		'lane_rouge_second';
		'lane_dogs';
		'lane_thief_leader';
	};
}

