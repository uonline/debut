		^
		-- Да совсем двинулась на старости лет.
		^
		-- А может, её кто двинул?
		^
		-- Сходить на рынок, расспросить людей?
		^
		Голоса переходят на шёпот, и тебе удаётся услышать, что бормочет старуха.
		Она повторяет лишь одно слово: метка.
	]];
}

-- Чёрная монета
black_coin = obj {
	nam = 'Чёрная монета';
	act = [[
		Ты рассматриваешь монету. Сначала ты принимаешь её за обычный
		медяк Режима, просто закопчённый дымом. Но разглядев её под светом,
		ты находишь множество отличий. Древо на гербе монеты лишено кроны --
		одни лишь скрюченные ветки. Вместо книги отчеканена непонятная куча.
		Другая сторона монеты отличается уже сильней. Вместо профиля Ремана
		Первого здесь красуется ухо, обрезанное сверху.
	]];
}
