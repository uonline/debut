-- Главарь воров
lane_thief_leader = obj {
	nam = 'Фигура в плаще';
	dsc = [[
		В конце переулка к стене привалилась {бесформенная фигура} в каких-то обносках.
	]];
	act = function()
		return [[
			Ты присматриваешься к фигуре. Тебе сложно определить кто это -- мужчина
			или женщина. Но судя по месту, которое этот кто-то занимает,
			это и должен быть торгаш.
		]];
	end;
	-- Даём торгашу бумаги
	used = function(self, what)
		if what == capitan_docs then
			-- Получаем монету от торговца
			take 'black_coin'
			-- Переходим в канализацию
			walk 'sewer'
			return [[
				Ты показательно собираешь пройти мимо, но из кучи обносок появляется рука
				и хватает тебя за запястье.
				^
				-- Пройдёмся, -- слышишь ты хриплый голос. Фигура демонстрирует часть лица,
				и ты узнаёшь тёмную щетину торгаша. Не говоря ни слова, ты суёшь ему
				кипу документов из комнаты капитана.
				^
				-- С козырей решил зайти? -- отзывается торгаш, начиная перелистывать бумаги,
				-- Печати Аберхейма. А это почерк Лейрина. И, кажется, всё это не подделка.
				Пресвятые Благие...
				^
				Он поспешно просматривает все документы.
				^
				-- Так, держи-ка это пока что при себе, -- он рывком прислонят бумаги к твоему
				нагруднику, и ты торопливо хватаешь их, чтобы письма не разлетелись.
				^
				-- Откуда они у тебя, лжережимник? -- спрашивает эта ходячая куча тряпья.
				^
				-- Одолжил у капитана штаба.
				^
				-- И я так понимаю, там что-то чертовски важное, раз ты прибежал с этим ко мне.
				Дезертир, ряженый в солдата, надеется на помощь. Не хочешь быть повешенным?
				^
				-- Не хочу погибнуть в очередной мясорубке.
				Не сегодня-завтра у стен города обнаружится банда урук-хай.
				А потом и армия лорда.
				^
				-- Его благородие наконец-то решило отобрать город у Режима,
				-- задумчиво произносит торгаш.
				^
				-- И навести здесь свои порядки, -- добавляешь ты.
				^
				-- А орки нужны ему, чтобы перерезать как можно больше режимников, -- продолжает
				рассуждать торгаш, -- и оправдать захват города.
				^
				-- Не уверен, что орки будут резать настолько избирательно. Кто знает, сколько будет жертв?
				^
				Твой попутчик останавливается.
				^
				-- Судя по всему, этот город не единственный? -- спрашивает он.
				^
				-- Один из первых. Лорды решили развязать новую войну.
				^
				-- Проклятье! -- торгаш сплёвывает под ноги, -- вот ведь дерьма куски...
				^
				Выругавшись, он подходит к куче какого-то хлама и начинает раскидывать его
				в стороны. -- Помоги-ка мне.
				^
				Вдвоём вы расчищаете завал и встаёте перед решёткой коллектора. Торгаш делает
				тебе знак рукой, и вы приподнимаете эту металлическую конструкцию и откидываете в
				сторону.
				^
				-- Это ещё что за казематы? -- интересуешься ты, с опаской с опаской глядя в чёрный
				зев тоннеля. Оттуда веет сыростью.
				^
				-- Канализация. Когда-то на месте этого города был другой, построенный ещё до
				древней войны с эльфами, -- поясняет торгаш, -- наследие прошлого. Ныне по назначению
				практически не используется. Жителей и знаний куда меньше.
				^
				-- И мы лезем туда? -- с отвращением спрашиваешь ты.
				^
				-- Не мы, а ты, -- поправляет тебя куча тряпья, -- мне нужно предупредить своих
				людей в гарнизоне. Постараемся приготовиться к встрече гостей.
				^
				-- Что мне делать там, внизу? И кто ты вообще такой?
				^
				-- Я вроде бы как заправляю здешним сбродом, который недоволен положением дел
				в городе и стране. Подполье, подонки, борцы за справедливость: нас называют по-разному.
				И я по доброте душевной приглашаю тебя к нам домой. Разыщи там старика-горбуна.
				До войны он входил в городской совет. А после оказался среди нас. Покажи ему бумаги.
				Может, он подскажет, что делать.
				^
				-- И где мне его там искать?
				^
				-- Сам не знаю. Порасспрашивай там всех, заодно познакомишься. Старик всё мечтал,
				чтобы его никто не трогал. Искал уединённое место. Пусть тебе подскажут, нашёл он его
				или нет.
				^
				-- А что потом?
				^
				-- Встречаемся в заброшенном тоннеле, там и обсудим. И вот, возьми, -- он суёт тебе что-то
				в ладонь. Этим чем-то оказывается монета, покрытая чёрной краской.
				^
				-- Покажешь это громиле-орку на входе. Скажешь, что пришёл от торговца чёрным деревом
				и книжным пеплом.
				^
				Набрав побольше воздуха в грудь, ты пролезаешь в люк и начинаешь спускаться по железным
				прутьям, закреплённым в кирпичной кладке колодца. Решётка над твоей головой с гулким
				скрипом встаёт на место. Повиснув на последнем пруте, ты разжимаешь руки и плюхаешься в
				какую-то вонючую жижу, едва устояв на ногах.
			]];
		end;
	end;
}

